var add = function (num1, num2) {
    // Add num1 and num2 together and return the value
    return num1 + num2
};

var subtract = function (num1, num2) {
    // Subtract num2 from num1 and return the value
    return num1 - num2
};

var multiply = function (num1, num2) {
    // Multiply num1 and num2 together and return the value
    return num1 * num2
};

var divide = function (num1, num2) {
    // Divide num1 by num2 and return the value
    return num1 / num2
};

console.log(add(10, 24)); // 34
console.log(subtract(100, 91)); // 9
console.log(multiply(7, 70)); // 490
console.log(divide(33, 11)); // 3